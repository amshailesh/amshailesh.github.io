        <h3>3D  floor installation stages</h3>
    <p><b>Thus, conventional methods of making the 3D floors  we need 
        :</b>
        <ul>
        <li>Component-based of a polymeric floor at the rate of approximately 4kg per 1 sq.m. surface. Depending on the selected component and fillers, the flow rate may be different.</li>
            <li>Hardener in the correct proportion of the selected substrate. And suitable for it.</li>
            <li>A solvent suitable for the selected polymer type.</li>
            <li>The drawing itself. We'll have to look for a printing house capable of making a design print on vinyl film or on fabric by thermal transfer on the right scale.</li>
        </ul>
        
        </p>
          
          <h3>Stage I- Select a 3D floor mural image</h3>
          <p>At first, select a 3D floor mural image that has the 3D floor design you want. It can be a photo or painting which make a 3D effect.  And now you need Photoshop. 3D flooring pictures must have a high resolution – at least 300 dpi.</p>
          
          <p>We highly recommend you not to use a 3D floor image for the with an illusion of motion. For example, something falls or wave covers.It’s really funny when near the pool in the bath guests suddenly notice absolutely natural toothy fish. But the monsters that really scare it is better not to use – it will affect negatively on the subconscious mind.</p>
          
          <h3>Stage II. Changing perspective of 3D floor art images</h3>
          
          <p>Step 1. So, the picture is selected, and the process is getting started. First of all take a photo of the room where you want to install 3D flooring – of course, with the “right” angle. But remember that many of the camera like a little distorted perspective – try to pick up the distance so that the photograph was exactly what you see with your own eyes at the threshold.</p>
          
          <p>Step 2. Now in Photoshop or a similar graphics program, apply your favorite 3D floor design image on the floor photo – just the way you want it in reality. So you have created the illusion of 3D flooring. Now fasten the two images into one – now, you should see the image of the room as volumetric 3D  floor design.</p>
          
          <h3>Stage 3- Printing the 3D flooring design image</h3>
          
          <p>So, as soon as the required file of the 3D floor mural is already on your flash drive, contact any agency of printing. Why there? Firstly, they have experience with similar graphics, and secondly, you need to fill material under the floor, which does not lose its properties and color when applying a varnish or a transparent epoxy resin flooring coating.</p>
          
          <p>Tip: be sure to print at first the same image of the 3D flooring design, but on plain paper, black and white – to put on the floor and assess whether the correct angle distortion, and indeed seems the same volume and the dolphin alive. If needed, adjust the perspective. And only then – you will pay for the main image of the 3D flooring murals you will use in your floor.</p>
          
          <p>Step 3. Now cut the everything in the photo except the floor with a picture. To complete the 3D flooring effect you can do it like a trapezoid: the bottom (front) extended at the top (behind) the restriction – because that is how we see the room.</p>
          
        <p>Step 4. Using the tool “Perspective” to make a correct the trapezoid. If you do not have coped with such a function – just drag it in the usual editor to make narrowest part. now the 3D floor murals are ready, save it.
Most importantly, what you should not miss in any way -it should not to be any stain of oil, grease, detergent or bitumen on the old concrete base . Clean oil stains from its full depth with milling, grinding or cutting.
</p>
          <h3>Stage V. Finishing coat</h3>
          
          <p>We recommend the most suitable coating for this is 3D Epoxy  which is the most stable of all known, and therefore your 3D floor designs will never fade. you should make 3mm layers under the 3D floor mural, each layer take around 8 hours to dry.</p>
          
          <p>Use a special glue to paste the photo on the floor to make a 3D flooring and then use a abrasion-resistant coating layer.</p>
          
          <p>3D technology developers hardly imagine how varied and interesting their ideas are for the future. Today 3D floors do in shopping malls, large halls, office buildings, apartments; many people event trying to do it with their own hands but its only can be don by specialists and quality materials. This is explained not only external beauty of the floor, but also its performance.</p>
          
