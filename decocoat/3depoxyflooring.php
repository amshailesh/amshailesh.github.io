        <h3>3D floor installation</h3><p>To get 3d flooring high-quality, we first need a perfectly flat surface, which can take up to 28 days to manufacture.<br><br>
        
        The 3D floor is a very thin (up to 3mm) layer of transparent polymer with a pattern inside it, deposited on a fabric or on a film.<br><br>
        
        
        With such a thickness of the filling layer, any unevenness will make the process of obtaining the 3d floor art impossible. Imagine that you will have sites that stick to the same three millimeters ... You will not feel this 3D floor anymore with your eyes, but with your feet ... Therefore, it is better not to remember the perfectly flat surface about the technology of liquid floors.<br><br>
        
        
