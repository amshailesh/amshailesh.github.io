        <p>3D floor murals are available on the internet and you can get yours from any online shop, but it’s difficult to find a detailed description of the 3D flooring technology itself. So, to take – and ready. How to make the base layer and how to pour that epoxy paint – this is not difficult to understand, but how to make the dolphin in the 3D floor looks alive – that’s where the secret. that’s you will learn in this article.</p>
        
