        <p>Epoxy flooring is made up of two main components – resins and hardeners. When these two components are combined, a chemical reaction takes place that forms a rigid epoxy material. This epoxy material bonds with the substrate. When it cures, it provides a strong, hard-wearing coating that protects concrete from both mechanical and chemical stress.The concrete is no longer porous, making it easy to clean.</p>
